float BNNModel::predict(const float* input) {
    float* temp;
    for (int i = 0; i < 2; i++) input_buffer[i] = input[i];

    float* current_in = input_buffer;
    float* current_out = hidden_buffer;
    int current_size = 2;

    // Linear layer 0
    for (int i = 0; i < 30; i++) {
        float acc = 0.0f;
        for (int j = 0; j < 2; j++) {
            acc += w0_mu[i][j] * current_in[j];
        }
        acc += b0_mu[i];
        current_out[i] = acc;
    }
    temp = current_in;
    current_in = current_out;
    current_out = temp;
    current_size = 30;

    // Activation relu
    for (int i = 0; i < current_size; i++) {
        if (current_in[i] < 0) current_in[i] = 0;
    }

    // Linear layer 2
    for (int i = 0; i < 3; i++) {
        float acc = 0.0f;
        for (int j = 0; j < 30; j++) {
            acc += w2_mu[i][j] * current_in[j];
        }
        acc += b2_mu[i];
        current_out[i] = acc;
    }
    temp = current_in;
    current_in = current_out;
    current_out = temp;
    current_size = 3;

    int best_idx = 0;
    float best_val = current_in[0];
    for (int i = 1; i < current_size; i++) {
        if (current_in[i] > best_val) {
            best_val = current_in[i];
            best_idx = i;
        }
    }
    return (float)best_idx;
}